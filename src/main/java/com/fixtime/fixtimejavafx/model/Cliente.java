package com.fixtime.fixtimejavafx.model;

import java.io.Serializable;

public class Cliente implements Serializable { // Indica que os objetos podem ser transformados em bytes, para salvar em arquivos
    private int id;
    private String nome;
    private int cpf;
    private int telefone;
    private String email;

    public Cliente(int id, String nome, int cpf, int telefone, String email) {
        this.id = id;
        this.nome = nome;
        this.cpf = cpf;
        this.telefone = telefone;
        this.email = email;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public int getTelefone() {
        return telefone;
    }

    public void setTelefone(int telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
